package com.example.proyectofinalannedecor.Clases.ConfiguracionTradicional;

public class Ganchos {
    String nombre;
    int idGanchos;

    public Ganchos(String nombre, int idGanchos) {
        this.nombre = nombre;
        this.idGanchos = idGanchos;

    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getIdGanchos() {
        return idGanchos;
    }

    public void setIdGanchos(int idGanchos) {
        this.idGanchos = idGanchos;
    }
}
